class CustomImageDataset(Dataset):
    @classmethod
    def use_decode_image(cls) -> bool:
        return True

    def __init__(
            self,
            img_dir: str,
            transform: T.Compose=None,
            extension: str=".jpg",
            to_rgb: bool = True,
            dataset_mode: bool = True,
            only_label_idx: bool = True,
            get_label_data: Callable = None,
            max_length_padding_filename: int = 40,
            read_image_with: str = "torchvision"
            ) -> None:
        #self.img_labels = pd.read_csv(annotations_file)
        self.img_dir = img_dir
        self.transform = transform
        self.extension = extension.lower()
        self.files = [f for f in os.listdir(img_dir) if f.lower().endswith(self.extension)]
        self.files = sorted(self.files)
        self.to_rgb = to_rgb
        self.dataset_mode = dataset_mode
        self.only_label_idx = only_label_idx
        self.get_label_data = get_label_data
        self.max_length_padding_filename = max_length_padding_filename
        self.read_image_with = read_image_with

    def __len__(self) -> int:
        return len(self.files)

    def __getitem__(self, idx: int) \
        ->  Tuple[torch.Tensor, int]|\
            Tuple[torch.Tensor, int, str, int]|\
            Tuple[torch.Tensor, torch.Tensor, int, str, int]:
        if idx >= len(self):
            return None, None, None
        
        image, image_trfm = self.get_image(idx=idx, to_rgb=self.to_rgb)
        if self.get_label_data:
            data = self.get_label_data(self.files[idx])
            label_idx = data[1]
            label_code = data[0]
        else:
            label_idx = None
            label_code = None
        
        if self.dataset_mode:
            if self.only_label_idx:
                # retourne l'image transformée et l'indice de la classe
                return image_trfm, label_idx 
            else:
                # retourn l'image transformée, l'indice de la classe, le code de la classe et l'indice du fichier
                return image_trfm, label_idx, label_code, idx  
        else:
            # retourn l'image initiale, l'image transformée, l'indice de la classe, le code de la classe et l'indice du fichier 
            return image, image_trfm, label_idx, label_code, idx

    def get_filenames(self, list_idx: List[int], with_path: bool = False) -> List[str]:
        return [self.files[i] for i in list_idx]

    def _get_image_with_torchvision(self, img_path: str, to_rgb: bool = True) -> torch.Tensor:
        image = decode_image(img_path, mode="RGB")
        # If the image is not a RGB but a 1 channel grey
        if to_rgb and image.size(0) == 1:
            #Create 3 same channels
            image = image.repeat(3, 1, 1)
        
        return image
    
    def _get_image_with_pil(self, img_path: str, to_rgb: bool = True) -> torch.Tensor:
        image = Image.open(img_path).convert("RGB")
        if to_rgb and len(image.getbands()) == 1:
            image_mono = image.convert("L")
            image = Image.merge("RGB", (image_mono, image_mono, image_mono))
        
        return image

    def get_image(self, idx: int = None, name: str = "", to_rgb: bool = True) -> Tuple[torch.Tensor, torch.Tensor]:
        assert name or idx != None, "No name or idx provided"
        assert name == "" or name in self.files, f"{name} unknown in this set"
        if idx != None and idx < 0:
            idx += len(self)
            assert idx < len(self), f"idx {idx} out of range [-{len(self)}, {len(self)}["

        name = name if name else self.files[idx]
        img_path = os.path.join(self.img_dir, name)
        
        if self.read_image_with.lower() == "torchvision":
            image = self._get_image_with_torchvision(img_path, to_rgb=to_rgb)
        elif self.read_image_with.lower() == "pil":
            image = self._get_image_with_pil(img_path, to_rgb=to_rgb)

        try:
            image_trfm = self.transform(image) if self.transform else None
        except Exception as e:
            print(f"Error while transforming image {name} ({idx}): {e}")
            image, image_trfm = None, None

        return image, image_trfm



